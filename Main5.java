// Código que verifica par ou impar
import java.util.*;
public class Main
{
	public static void main(String[] args) {
	      int numero;
	      numero = 4;
	      
	       if(numero % 2 == 0){
	           System.out.println("O número é par");
	       }else{
	           System.out.println("O número é impar");
	       }
	}
}
